async function checkPages() {
  const browser = await puppeteer.launch({
    headless: 'new',
    executablePath: '/nix/store/zi4f80l169xlmivz8vja8wlphq74qqk0-chromium-125.0.6422.141/bin/chromium',
    args: ['--no-sandbox', '--disable-setuid-sandbox', '--disable-dev-shm-usage'],
    defaultViewport: { width: 1920, height: 1080 }
  });

  const page = await browser.newPage();
  const notFoundPages = [];

  for (let i = 0; i < routes.length; i++) {
    const route = routes[i];
    try {
      await page.goto(`http://localhost:5000${route.path}`, { waitUntil: 'domcontentloaded', timeout: 8000 });
      await new Promise(r => setTimeout(r, 500));
      
      const content = await page.evaluate(() => document.body.innerText);
      if (content.includes('404') || content.includes('Not Found') || content.includes('Page not found') || content.includes('not-found')) {
        notFoundPages.push({ num: i + 1, ...route });
        console.log(`✗ ${route.name} - 404`);
      }
    } catch (err) {
      notFoundPages.push({ num: i + 1, ...route, error: err.message.slice(0, 50) });
      console.log(`✗ ${route.name} - Error`);
    }
  }

  await browser.close();
  
  console.log('\n========== 404 PAGES SUMMARY ==========');
  if (notFoundPages.length === 0) {
    console.log('All 235 pages are working correctly!');
  } else {
    console.log(`Found ${notFoundPages.length} pages with issues:\n`);
    notFoundPages.forEach(p => console.log(`${p.num}. ${p.path} (${p.name})`));
  }
}

